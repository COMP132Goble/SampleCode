package IOExamples;

public class Basics {
    public static void main(String[] args) {
        System.out.print("Hello, World");       // no new line
        System.out.println("Hello\n\nWorld");   // new line at the end

        int feet = 6, inches = 0;
        System.out.printf("I am %d feet and %d inches tall\n", feet, inches);
    }
}
